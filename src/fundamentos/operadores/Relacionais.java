package fundamentos.operadores;

public class Relacionais {

	public static void main(String[] args) {
		//Relacionais são binários (possui 2 operandos X + Y, por exemplo)
		//sempre vão retornar true ou false
		
		int a = 1;
		int b = 2;
		
		System.out.println(a == b);//"=="Igual a
		
		System.out.println(4 > 2); //">"Maior que
		System.out.println(1 < 4);//"<"Menor que
		System.out.println(7 >= 30);//>="Maior ou igual que
		System.out.println(90 <= 15);//"<="Menor ou igual que
		System.out.println(20 != 2);//"!="Diferente de
	}
	
}
