package fundamentos.operadores;

public class Atribuicao {

	public static void main(String[] args) {
		//atribuição é adicionar algo, um valor em uma variável
		
		int a = 1;
		int b = 2;
		
		a = a + b;
		a += b;//mesmo procedimento acima, porém simplificado
		
		a *= b;
		a /= b;
		a %= 2;//modular, irá resultar 0 ou 1, 0 é par e 1 é impar
		
		a++; //soma 1 na variável
		
		
	}
}
